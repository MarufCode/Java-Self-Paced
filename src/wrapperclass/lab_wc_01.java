package src.wrapperclass;

public class lab_wc_01 {

    public static void main(String[] args) {


        int a = 10; // primitive Data Type



        // Wrapper classes are below:

        Integer a2 = 30;
        Integer a3 = 50;


        // Collection framework likes classes and classes hate primitive data types
        // We will use wrapper classes in collection framework

        System.out.println(Integer.MIN_VALUE);
        System.out.println(Integer.MAX_VALUE);
        System.out.println(Integer.max(a2, a3));
        System.out.println(Integer.min(45, 56));

    }

}
